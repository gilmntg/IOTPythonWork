class TadiranACRemoteCmd(object):
	def __init__( self, irbuf, dest_dev_id):
		self.irbuf = irbuf
		self.offset = 0
		self.dev_id = dest_dev_id

	def send_using_client( self, mqtt_client ):
		IOTCmd.IrRawBeginIOTCmd( self.dev_id ).send_using_client( mqtt_client ) 
		for i in xrange(len(self.irbuf)/10):
			payload = ''
			for j in xrange(10):
				payload += '%d ' % abs(self.irbuf[self.offset])
				self.offset+=1
			IOTCmd.IrRawPartIOTCmd( self.dev_id, payload ).send_using_client ( mqtt_client )
			
		if self.offset < len(self.irbuf):
			payload = ''
			for j in xrange(len(self.irbuf)-self.offset):
				payload += '%d ' % abs(self.irbuf[self.offset])
				self.offset += 1
			IOTCmd.IrRawPartIOTCmd( self.dev_id, payload ).send_using_client ( mqtt_client )

		IOTCmd.IrRawEndIOTCmd( self.dev_id ).send_using_client ( mqtt_client )
			

